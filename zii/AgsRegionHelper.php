	public function initDb()
	{
		echo '<pre>';
		foreach ($this->regionDb as $e)
		{
			$regionAr = new $this->regionArClass;
			$region = $regionAr->findByAttributes(array('name'=>$e['name']));
			if (null === $region)
			{
				if (isset($e['parent']) && null != ($parent = $regionAr->findByAttributes(array('name'=>$e['parent']))))
				{
					unset($e['parent']);
					$e['parentId'] = $parent->id;
				}
				else{
					$e['parentId'] = 0;
				}

				$region = new $regionAr;
				$region->attributes = $e;
				if ($region->save())
				{
					echo $region->name.' saved.<br />==============<br />';
				}
				else
				{
					echo 'Failed to save '.$region->name.'.<br />';
					var_export($region->getErrors());
					echo '==============<br />';
				}
			}
			else
			{
				echo $region->name.' exists.<br />==============<br />';
			}
		}
		echo '</pre>';
	}
