	public static function initDb($regionArClass)
	{
		echo '<pre>';
		foreach (self::$regionDb as $id=>$name)
		{
			$regionAr = new $regionArClass;
			$region = $regionAr->findByAttributes(array('name'=>$name));
			if (!$region)
			{
				$region = new $regionAr;
				$region->name = $name;
				$region->id = $id;

				$parentId = intval($id/100);
				if (key_exists($parentId,self::$regionDb)
					&& ($parent = $regionAr->findByAttributes(array('name'=>self::$regionDb[$parentId]))))
				{
					$region->parentId = $parentId;
				}

				if ($region->save())
				{
					echo $region->name.' saved.<br />==============<br />';
				}
				else
				{
					echo 'Failed to save '.$region->name.'.<br />';
					var_export($region->getErrors());
					echo '==============<br />';
				}
			}
			else
			{
				echo $region->name.' exists.<br />==============<br />';
			}
		}
		echo '</pre>';
	}
